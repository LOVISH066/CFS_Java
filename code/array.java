class array {
    public static void main(String[] args) {
        // creating array
        int arr[] = new int[5];

        // assigning value in array
        arr[0] = 10;
        arr[1] = 20;
        arr[2] = 30;
        arr[4] = 40;

        // printing of array
        System.out.println(arr[0]);
        System.out.println(arr[1]);
        System.out.println(arr[2]);
        System.out.println(arr[3]);
    }    
}
