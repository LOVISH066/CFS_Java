class relation{

    public static void main(String[] args)
    {
        int a = 10;
        int b = 10;
        // Relational operator
        System.out.println(a==b); // true
        System.out.println(a!=b); // false
        System.out.println(a>b); // false
        System.out.println(a<b); // false
        System.out.println(a>=b); // true
        System.out.println(a<=b); // true


    }
}